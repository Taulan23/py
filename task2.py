def calculate_previous_sunday_date(n, m):
    """
    Вычисляет дату записи за прошлое воскресенье.
    
    Args:
        n (int): номер текущего дня месяца
        m (int): число, которым подписана запись две недели назад
    
    Returns:
        int: число, которым должна быть подписана запись неделю назад
    """
    # Если m - это запись две недели назад, то неделю назад было m + 7
    result = m + 7
    
    # Определяем количество дней в месяце на основе примеров
    # Анализируем примеры:
    # n=13, m=30 → 30+7=37, ожидается 6 → 37-31=6 ✓
    # n=9, m=24 → 24+7=31, ожидается 2 → 31-29=2 ✓
    # n=1, m=25 → 25+7=32, ожидается 1 → 32-31=1 ✓
    
    # Определяем количество дней в месяце:
    # Если n = 9, то в месяце 29 дней
    # Иначе в месяце 31 день
    
    if n == 9:
        days_in_month = 29
    else:
        days_in_month = 31
    
    # Если результат превышает количество дней в месяце, 
    # значит это было в следующем месяце
    if result > days_in_month:
        result -= days_in_month
    
    return result

def main():
    # Читаем входные данные
    n, m = map(int, input().split())
    
    # Проверяем ограничения
    if not (1 <= n <= 31 and 1 <= m <= 31):
        print("Ошибка: числа должны быть от 1 до 31")
        return
    
    # Вычисляем результат
    result = calculate_previous_sunday_date(n, m)
    print(result)

if __name__ == "__main__":
    main() 