def is_honestly_even(n):
    """
    Проверяет, является ли число честно чётным.
    Честно чётное число - это число, у которого все цифры чётные.
    """
    # Преобразуем число в строку для проверки каждой цифры
    num_str = str(n)
    
    # Проверяем каждую цифру на чётность
    for digit in num_str:
        if int(digit) % 2 != 0:
            return 0  # Найдена нечётная цифра
    
    return 1  # Все цифры чётные

def main():
    # Читаем входные данные
    n = int(input())
    
    # Проверяем ограничение
    if n >= 10**9:
        print("Ошибка: число должно быть меньше 10^9")
        return
    
    # Получаем результат
    result = is_honestly_even(n)
    print(result)

if __name__ == "__main__":
    main() 