#!/bin/bash

# Проверяем наличие виртуальной среды
if [ ! -d "venv" ]; then
    echo "❌ Виртуальная среда не найдена. Запустите ./install.sh для установки"
    exit 1
fi

# Активируем виртуальную среду
source venv/bin/activate

# Запускаем программу с переданными аргументами
python 123456789000.py "$@" 